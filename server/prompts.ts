export function buildUserPrompt(description: string, fixHistory: FixRequestInput[]): string {
  const fixes =
    fixHistory.length === 0
      ? "Жоқ."
      : fixHistory
          .map((f, i) => `${i + 1}. ${f.message}`)
          .join("\n");

  return `Мұғалімнің ойын сипаттамасы:
"""
${description}
"""

Түзету сұраулары:
${fixes}

Бір index.html интерактивті ойынмен және әдемі CSS-дизайнмен жаса. Тек HTML-код қайтар, <!DOCTYPE html> басталады.`;
}
